// 编译指令：g++ objPointer.cpp -o objPointer
// 运行指令：./objPointer

#include <iostream>
using namespace std;

// 定义一个普通的类A
class A
{
public:
    // 类的公有成员变量：两个整型变量
    int var1, var2;

    // 类的公有成员函数：计算var1和var2的和并返回
    int add()
    {
        return var1 + var2;
    }
};

int main()
{
    // 创建类A的一个实例（对象）ex
    A ex;

    // 给对象ex的成员变量赋值
    ex.var1 = 3;
    ex.var2 = 4;

    // 定义普通指针p，指向对象ex的成员变量var1的地址
    // 这是指向「成员变量」的普通指针，和常规指针用法一致
    int *p = &ex.var1;
    // 解引用指针p，输出var1的值（结果为3）
    cout << *p << endl;

    // 定义「类成员函数指针」fun_p
    // 语法解析：
    // int：成员函数的返回值类型
    // A::*fun_p：表示fun_p是属于类A的成员函数指针
    // ()：成员函数add无参数
    int (A::*fun_p)();

    // 将类A的add成员函数地址赋值给函数指针fun_p
    // 注意：类成员函数指针赋值时，必须显式加 & 符号（和普通函数指针不同）
    fun_p = &A::add;

    // 通过成员函数指针调用add函数
    // 语法解析：
    // ex.*fun_p：通过对象ex调用其成员函数指针fun_p指向的函数
    // ()：传递参数（这里add无参，所以是空括号）
    // 整体效果等价于 ex.add()，结果为3+4=7
    cout << (ex.*fun_p)() << endl;
    cout << ex.add() << endl;

    return 0;
}

// 普通函数名（如之前的add）本身就是函数在内存中的入口地址常量，
// 编译器会自动将 fun_p = add 解析为 fun_p = &add，所以 & 可以省略。

// 1. 加 & 的原因：
/*
    类的成员函数（如A::add）并不是 “全局地址常量”—— 它属于类的作用域，而非直接的内存地址。
    编译器无法像普通函数那样，把A::add直接等同于 “地址”，必须通过&A::add显式告诉编译器：“我要取这个成员函数的地址，而非调用它或做其他操作”。简单说：A::add是 “函数的名字（作用域内标识）”，&A::add才是 “函数的地址（指针所需的数值）”，语法上必须显式区分。
    */

// 2. 绑定类 + 指定对象的原因：
// 成员函数依赖 this 指针访问对象的成员变量，必须先明确所属类（确定 this 指针类型），再指定具体对象（传递 this 指针的具体值），否则无法完成函数调用。