// 编译指令：g++ funcPointer.cpp -o funcPointer
// 运行指令：./funcPointer

#include <iostream>
using namespace std;

// 定义一个普通的加法函数
// 参数：两个int类型的整数a和b
// 返回值：a和b的和
int add(int a, int b)
{
    return a + b;
}

int main(void)
{
    // 定义一个函数指针变量fun_p
    // 类型说明：指向“接收两个int参数、返回int值”的函数的指针
    int (*fun_p)(int, int) = add;

    // 将加法函数add的地址赋值给函数指针fun_p
    // 函数名本身就是函数的入口地址，无需加&（加&也可以，效果相同）
    fun_p = &add;

    // 也可一步到位，直接在定义时初始化函数指针
    // int (*fun_p)(int, int) = add;

    // 通过函数指针调用add函数，传入参数1和6，输出结果
    // 等价于调用add(1,6)，结果为7
    cout << fun_p(1, 6) << endl;

    return 0;
}

/* 语法拆解 */
// 部分	                                   含义
// int	                   函数指针指向的函数的返回值类型（这里是 int）
// (*fun_p)	               fun_p是一个指针（括号必须加，否则会被解析为 “返回值是 int * 的函数”）
// (int, int)	           函数指针指向的函数的参数列表（两个 int 类型参数）

/* 核心用途 */
// 1. 回调函数：比如在排序、遍历中，把自定义逻辑（函数）通过指针传给通用函数（如 C 标准库的 qsort）；
// 2. 动态函数调用：运行时根据条件选择调用不同函数（比如不同的计算逻辑）；
// 3. 封装函数逻辑：简化多函数调用的代码，提高灵活性。

// 注：
// 指针函数本质是一个函数，只不过该函数的返回值是一个指针。相对于普通函数而言，只是返回值是指针。
// 函数指针本质是一个指针变量，只不过这个指针指向一个函数。函数指针即指向函数的指针。