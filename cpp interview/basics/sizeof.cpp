// 编译指令：g++ sizeof.cpp -o sizeof
// 运行指令：./sizeof

#include <iostream>
using namespace std;

int main()
{
    // 核心分析：
    // 1. 1/0 是算术表达式（除法运算），其结果的类型为 int（C/C++算术运算默认类型）；
    // 2. sizeof 是编译期运算符，仅判断表达式结果的类型，不会实际执行 1/0（因此不会触发除零运行时错误）；
    // 3. C++中 int 类型在绝大多数系统中占4字节，因此最终输出4；
    // 4. 注意：1/0 不是布尔表达式（布尔表达式是==/>/<等判断类表达式，返回bool类型）
    cout << sizeof(1 / 0) << endl;
    return 0;
}

// 注：
// 1. 运行结果：4（因为系统中 int 占4字节）；
// 2. 对比布尔表达式：sizeof(1==1) 在C++中返回1（bool占1字节），在C中返回4（C早期无bool，布尔表达式返回int）；
// 3. 关键区别：1/0 是算术表达式（类型int），1==1 是布尔表达式（C++返回bool/C返回int）。