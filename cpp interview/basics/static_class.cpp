// 编译指令：g++ static_class.cpp -o static_class
// 运行指令：./static_class

#include <iostream>
using namespace std;

/**
 * @brief 演示C++类的静态成员变量/静态成员函数的核心特性
 * @details 1. 静态成员变量：属于类本身，所有对象共享，需在类外初始化
 *          2. 静态成员函数：属于类本身，无this指针，不能访问非静态成员
 */
class A
{
private:
    // 非静态成员变量（实例变量）：每个对象独立拥有一份
    int var;
    // 静态成员变量（类变量）：所有对象共享，属于类本身，需在类外初始化
    static int s_var;

public:
    /**
     * @brief 非静态成员函数：可访问静态/非静态成员
     * @details 有this指针，指向当前对象实例
     */
    void show()
    {
        // 访问静态成员变量s_var
        cout << s_var++ << endl;
    }

    /**
     * @brief 静态成员函数：仅能访问静态成员，不能访问非静态成员
     * @details 无this指针，不依赖具体对象实例，可通过类名直接调用
     * 静态成员函数不能声明成虚函数（virtual）、const 函数和 volatile 函数
     */
    static void s_show()
    {
        // ✅ 合法：静态成员函数可以访问静态成员变量
        cout << s_var << endl;

        // ❌ 非法：静态成员函数无this指针，不能访问非静态成员变量
        // cout << var << endl;

        // ❌ 非法：静态成员函数不能调用非静态成员函数（非静态函数依赖对象实例）
        // show();
    }
};

// 静态成员变量的类外初始化（必须！否则编译报错）
// 语法：数据类型 类名::静态变量名 = 初始值; （默认初始化为0，此处显式赋值为1）
int A::s_var = 1;

int main()
{
    // ❌ 非法：s_var是私有静态成员，类外部无法直接访问
    // cout << A::s_var << endl;

    // 创建类A的实例对象ex
    A ex;
    // 调用非静态成员函数：必须通过对象实例调用
    ex.show(); // 输出1（s_var自增前的值），执行后s_var变为2

    // 调用静态成员函数：推荐通过类名调用（也可通过对象调用，但不规范）
    A::s_show(); // 输出2（当前s_var的值）

    // 补充：也可以通过对象调用静态成员函数（不推荐，易混淆）
    ex.s_show(); // 同样输出2，合法但不符合编码规范

    return 0;
}

